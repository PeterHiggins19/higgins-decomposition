def run_stage2(compositions, carrier_names, day_ids=None, point_ids=None,
               output_dir="."):
    """Run complete Stage 2 cross-examination.

    Args:
        compositions: list of N raw composition vectors
        carrier_names: list of D carrier names
        day_ids: list of N day identifiers
        point_ids: list of N point identifiers
        output_dir: directory for output files

    Returns:
        dict with all results for Stage 3 consumption
    """
    os.makedirs(output_dir, exist_ok=True)

    # Build Stage 1 data container
    data = Stage1Data(compositions, carrier_names, day_ids, point_ids)

    # 2.1 Group barycenter analysis
    group_results = group_barycenter_analysis(data)

    # 2.2 Pairwise group cross-examination
    group_pw = pairwise_group_cross_examination(data, group_results)

    # 2.3 Carrier pair cross-examination
    carrier_pw = carrier_pair_cross_examination(data)

    # 2.4 Section view cross-examination
    section_pw = section_view_cross_examination(data)

    # Write outputs
    write_group_ledger(os.path.join(output_dir, "stage2_group_ledger.csv"), group_results)
    write_pairwise_group_ledger(os.path.join(output_dir, "stage2_pairwise_group_ledger.csv"), group_pw)
    write_carrier_pair_ledger(os.path.join(output_dir, "stage2_carrier_pair_ledger.csv"), carrier_pw)
    write_section_view_ledger(os.path.join(output_dir, "stage2_section_view_ledger.csv"), section_pw)
    write_ranked_findings(os.path.join(output_dir, "stage2_ranked_findings.json"),
                          group_pw, carrier_pw, section_pw)
    write_summary_report(os.path.join(output_dir, "stage2_summary_report.txt"),
                         data, group_results, group_pw, carrier_pw, section_pw)

    return {
        "data": data,
        "group_results": group_results,
        "group_pairwise": group_pw,
        "carrier_pairwise": carrier_pw,
        "section_pairwise": section_pw,
    }
