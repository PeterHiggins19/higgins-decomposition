def group_barycenter_analysis(data, groups=None):
    """Stage 2.1 — Aggregate points into groups and compute barycenters.

    If groups is None, groups by day_id.
    Returns list of group records.
    """
    if groups is None:
        days = data.unique_days()
        groups = {d: data.get_day_points(d) for d in days}

    results = []
    for group_id, indices in groups.items():
        if not indices:
            continue

        h_list = [data.h[i] for i in indices]
        x_list = [data.x[i] for i in indices]
        bary = _barycenter(h_list)
        energies = [data.energies[i] for i in indices]
        pressures = [data.boundary_pressures[i] for i in indices]

        # Helmsman count by carrier
        helm_counts = {}
        for i in indices:
            c = data.dcdi_carriers[i]
            if c is not None:
                helm_counts[c] = helm_counts.get(c, 0) + 1

        # Dominant carrier = most frequent helmsman
        dominant = max(helm_counts, key=helm_counts.get) if helm_counts else "---"

        results.append({
            "group_id": str(group_id),
            "group_size": len(indices),
            "group_barycenter_vector": bary,
            "group_metric_energy": sum(energies) / len(energies),
            "group_boundary_pressure": max(pressures),
            "dominant_carrier": dominant,
            "helmsman_count_by_carrier": helm_counts,
        })

    return results


def pairwise_group_cross_examination(data, group_results):
    """Stage 2.2 — Compare all group pairs under the Higgins metric.

    Returns list of pairwise comparison records, ranked by distance.
    """
    pairs = list(combinations(range(len(group_results)), 2))
    results = []

    for rank_idx, (i, j) in enumerate(pairs):
        ga = group_results[i]
        gb = group_results[j]

        ba = ga["group_barycenter_vector"]
        bb = gb["group_barycenter_vector"]

        dist = _metric_distance(ba, bb)
        angle = _metric_angle(ba, bb)
        energy_delta = abs(ga["group_metric_energy"] - gb["group_metric_energy"])

        # Dominant contrast carrier: which carrier dimension has max barycenter difference
        D = len(ba)
        carrier_deltas = [(abs(ba[k] - bb[k]), k) for k in range(D)]
        carrier_deltas.sort(reverse=True)
        dom_carrier = data.carrier_names[carrier_deltas[0][1]]

        pressure_delta = abs(ga["group_boundary_pressure"] - gb["group_boundary_pressure"])

        results.append({
            "pair_id": f"P{rank_idx + 1:03d}",
            "group_A": ga["group_id"],
            "group_B": gb["group_id"],
            "metric_distance": dist,
            "higgins_angle": angle,
            "metric_energy_delta": energy_delta,
            "dominant_contrast_carrier": dom_carrier,
            "boundary_pressure_delta": pressure_delta,
            "rank": 0,  # filled after sorting
            "classification": "",
        })

    # Rank by metric distance (largest first)
    results.sort(key=lambda r: r["metric_distance"], reverse=True)
    for i, r in enumerate(results):
        r["rank"] = i + 1
        if r["metric_distance"] > 1.0:
            r["classification"] = "STRONG_DIVERGENCE"
        elif r["metric_distance"] > 0.3:
            r["classification"] = "MODERATE_DIVERGENCE"
        else:
            r["classification"] = "WEAK_DIVERGENCE"

    return results


def carrier_pair_cross_examination(data):
    """Stage 2.3 — Compare carrier channels pairwise across the full run.

    Examines co-movement, opposition, and metric coupling for all
    D*(D-1)/2 carrier pairs.
    """
    D = data.D
    N = data.N
    carrier_pairs = list(combinations(range(D), 2))
    results = []

    for ci, cj in carrier_pairs:
        # Extract carrier-level CLR series
        hi_series = [data.h[t][ci] for t in range(N)]
        hj_series = [data.h[t][cj] for t in range(N)]

        # Co-movement: correlation of CLR increments
        if N > 1:
            dhi = [hi_series[t + 1] - hi_series[t] for t in range(N - 1)]
            dhj = [hj_series[t + 1] - hj_series[t] for t in range(N - 1)]

            mean_dhi = sum(dhi) / len(dhi)
            mean_dhj = sum(dhj) / len(dhj)

            cov = sum((dhi[t] - mean_dhi) * (dhj[t] - mean_dhj) for t in range(len(dhi)))
            var_i = sum((dhi[t] - mean_dhi) ** 2 for t in range(len(dhi)))
            var_j = sum((dhj[t] - mean_dhj) ** 2 for t in range(len(dhj)))

            denom = math.sqrt(max(var_i, 1e-300) * max(var_j, 1e-300))
            correlation = cov / denom if denom > 1e-300 else 0.0
        else:
            correlation = 0.0

        co_movement = max(0.0, correlation)
        opposition = max(0.0, -correlation)

        # Metric coupling: average off-diagonal G_ij across all points
        couplings = []
        for t in range(N):
            G = _metric_tensor(data.x[t])
            couplings.append(abs(G[ci][cj]))
        metric_coupling = sum(couplings) / len(couplings)

        # Dominant day: day with largest combined displacement in both carriers
        days = data.unique_days()
        day_scores = {}
        for day in days:
            pts = data.get_day_points(day)
            score = 0.0
            for t in pts:
                if t > 0 and data.day_ids[t - 1] == day:
                    score += abs(data.h[t][ci] - data.h[t - 1][ci])
                    score += abs(data.h[t][cj] - data.h[t - 1][cj])
            day_scores[day] = score

        dominant_day = max(day_scores, key=day_scores.get) if day_scores else "---"

        results.append({
            "carrier_pair": f"{data.carrier_names[ci]}-{data.carrier_names[cj]}",
            "carrier_i": ci,
            "carrier_j": cj,
            "co_movement_score": co_movement,
            "opposition_score": opposition,
            "metric_coupling_score": metric_coupling,
            "dominant_day": str(dominant_day),
            "dominant_section_view": "XY",  # primary compositional plan
            "rank": 0,
            "classification": "",
        })

    # Rank by opposition score (strongest opposition first)
    results.sort(key=lambda r: r["opposition_score"], reverse=True)
    for i, r in enumerate(results):
        r["rank"] = i + 1
        if r["opposition_score"] > 0.7:
            r["classification"] = "STRONG_OPPOSITION"
        elif r["opposition_score"] > 0.3:
            r["classification"] = "MODERATE_OPPOSITION"
        elif r["co_movement_score"] > 0.7:
            r["classification"] = "STRONG_CO_MOVEMENT"
        elif r["co_movement_score"] > 0.3:
            r["classification"] = "MODERATE_CO_MOVEMENT"
        else:
            r["classification"] = "WEAK_COUPLING"

    return results


def section_view_cross_examination(data):
    """Stage 2.4 — Compare XY, XZ, YZ section behavior.

    For compositions with D >= 3, examines how different carrier-pair
    planes capture the system's structure.
    """
    D = data.D
    N = data.N

    if D < 3:
        return []

    # Define section views as carrier-pair planes
    # XY = carriers (0, 1), XZ = carriers (0, 2), YZ = carriers (1, 2)
    views = [
        ("XY", 0, 1),
        ("XZ", 0, 2),
        ("YZ", 1, 2),
    ]

    # Compute per-view variance across all time points
    view_variances = {}
    for name, ci, cj in views:
        # Project each CLR vector onto the 2D plane of carriers ci, cj
        projected_dists = []
        for t in range(N):
            r = math.sqrt(data.h[t][ci] ** 2 + data.h[t][cj] ** 2)
            projected_dists.append(r)

        mean_r = sum(projected_dists) / N
        var_r = sum((r - mean_r) ** 2 for r in projected_dists) / N
        view_variances[name] = var_r

        # Dominant carrier in this view
        var_ci = sum((data.h[t][ci] - sum(data.h[tt][ci] for tt in range(N)) / N) ** 2
                     for t in range(N)) / N
        var_cj = sum((data.h[t][cj] - sum(data.h[tt][cj] for tt in range(N)) / N) ** 2
                     for t in range(N)) / N
        view_variances[f"{name}_dom"] = data.carrier_names[ci] if var_ci > var_cj else data.carrier_names[cj]

    # Compare view pairs
    view_pairs = list(combinations(views, 2))
    results = []

    for (name_a, ai, aj), (name_b, bi, bj) in view_pairs:
        var_a = view_variances[name_a]
        var_b = view_variances[name_b]

        total = var_a + var_b
        agreement = 1.0 - abs(var_a - var_b) / max(total, 1e-300)
        divergence = abs(var_a - var_b) / max(total, 1e-300)

        dom_carrier = view_variances[f"{name_a}_dom"] if var_a > var_b else view_variances[f"{name_b}_dom"]

        # Dominant day: max per-day variance difference between the two views
        days = data.unique_days()
        best_day = days[0] if days else "---"
        best_diff = 0.0
        for day in days:
            pts = data.get_day_points(day)
            if len(pts) < 2:
                continue
            h_day = [data.h[t] for t in pts]
            va = sum(h[ai] ** 2 + h[aj] ** 2 for h in h_day) / len(h_day)
            vb = sum(h[bi] ** 2 + h[bj] ** 2 for h in h_day) / len(h_day)
            diff = abs(va - vb)
            if diff > best_diff:
                best_diff = diff
                best_day = day

        results.append({
            "view_pair": f"{name_a}-{name_b}",
            "agreement_score": agreement,
            "divergence_score": divergence,
            "dominant_carrier": dom_carrier,
            "dominant_day": str(best_day),
            "rank": 0,
            "classification": "",
        })

    results.sort(key=lambda r: r["divergence_score"], reverse=True)
    for i, r in enumerate(results):
        r["rank"] = i + 1
        if r["divergence_score"] > 0.5:
            r["classification"] = "STRONG_VIEW_DIVERGENCE"
        elif r["divergence_score"] > 0.2:
            r["classification"] = "MODERATE_VIEW_DIVERGENCE"
        else:
            r["classification"] = "CONSISTENT_VIEWS"

    return results


