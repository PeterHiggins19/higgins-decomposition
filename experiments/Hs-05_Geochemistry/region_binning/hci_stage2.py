class Stage1Data:
    """Container for Stage 1 outputs consumed by Stage 2."""

    def __init__(self, compositions, carrier_names, day_ids=None, point_ids=None):
        """
        compositions: list of N raw composition vectors (each length D)
        carrier_names: list of D carrier names
        day_ids: list of N day identifiers (int or str)
        point_ids: list of N point identifiers within each day
        """
        self.carrier_names = carrier_names
        self.D = len(carrier_names)
        self.N = len(compositions)

        # Close and compute Higgins coordinates
        self.x = [_closure(c) for c in compositions]
        self.h = [_clr(xi) for xi in self.x]

        # Day/point indexing
        self.day_ids = day_ids if day_ids else [i for i in range(self.N)]
        self.point_ids = point_ids if point_ids else list(range(self.N))

        # Precompute per-point diagnostics
        self.energies = [_metric_energy(hi) for hi in self.h]
        self.boundary_pressures = [_boundary_pressure(xi) for xi in self.x]

        # DCDI for each consecutive pair
        self.dcdi_carriers = []
        self.dcdi_deltas = []
        for i in range(self.N):
            if i == 0:
                self.dcdi_carriers.append(None)
                self.dcdi_deltas.append(0.0)
            else:
                idx, name, delta = _dcdi(self.h[i - 1], self.h[i], carrier_names)
                self.dcdi_carriers.append(name)
                self.dcdi_deltas.append(abs(delta))

    def get_day_points(self, day_id):
        """Return indices of all points belonging to a given day."""
        return [i for i in range(self.N) if self.day_ids[i] == day_id]

    def unique_days(self):
        """Return sorted unique day identifiers."""
        seen = []
        for d in self.day_ids:
            if d not in seen:
                seen.append(d)
        return seen


