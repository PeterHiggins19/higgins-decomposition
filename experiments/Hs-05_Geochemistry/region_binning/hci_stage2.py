#!/usr/bin/env python3
"""
HCI Stage 2 — Metric Cross-Examination Engine
===============================================

Performs pairwise and group-level metric cross-examinations
on Stage 1 section-atlas outputs.

Input:  Stage 1 metric ledgers (master_metric_ledger, carrier_ledger, triad_ledger)
Output: CSV ledgers + JSON ranked findings + TXT summary report

The engine does not create a new geometry. It uses the shared Higgins
coordinate frame and shared local Higgins Steering Metric Tensor
generated by Stage 1.

Terminology standard:
  - Dominant Carrier Displacement Index (DCDI) = formal operator
  - Helmsman Index = HCI instrument alias
  - Parallel carrier channels = processing lanes
  - Metric ledger = numerical authority

Mathematical lineage:
  Aitchison (1986) — CLR, Aitchison distance
  Egozcue et al. (2003) — ILR, orthonormal coordinates
  Higgins (2026) — CNT, HCI, Higgins Steering Metric Tensor

The instrument reads. The expert decides. The loop stays open.
"""

import math
import csv
import json
import os
from itertools import combinations


# ══════════════════════════════════════════════════════════════════
# SHARED GEOMETRY (imported from Stage 1 / hci_cbs.py)
# ══════════════════════════════════════════════════════════════════

def _closure(x, eps=1e-15):
    """Simplex closure."""
    x_pos = [max(float(v), eps) for v in x]
    total = sum(x_pos)
    return [v / total for v in x_pos]


def _clr(x):
    """CLR transform on closed composition."""
    D = len(x)
    log_x = [math.log(v) for v in x]
    mean_log = sum(log_x) / D
    return [lx - mean_log for lx in log_x]


def _metric_tensor(x, eps=1e-15):
    """Higgins Steering Metric Tensor kappa^Hs_ij(x)."""
    x = _closure(x, eps=eps)
    D = len(x)
    G = []
    for i in range(D):
        row = []
        for j in range(D):
            delta = 1.0 if i == j else 0.0
            row.append((delta - 1.0 / D) / (x[i] * x[j]))
        G.append(row)
    return G


def _metric_distance(h1, h2):
    """Euclidean distance in CLR space = Aitchison distance."""
    return math.sqrt(sum((a - b) ** 2 for a, b in zip(h1, h2)))


def _metric_angle(h1, h2):
    """Angle between CLR vectors in degrees (atan2 stable form)."""
    dot = sum(a * b for a, b in zip(h1, h2))
    m1_sq = sum(a ** 2 for a in h1)
    m2_sq = sum(a ** 2 for a in h2)
    if m1_sq < 1e-300 or m2_sq < 1e-300:
        return 0.0
    cross_sq = max(0.0, m1_sq * m2_sq - dot * dot)
    return math.degrees(math.atan2(math.sqrt(cross_sq), dot))


def _metric_energy(h):
    """||h||^2 — displacement energy from barycenter."""
    return sum(v ** 2 for v in h)


def _barycenter(h_list):
    """Arithmetic mean of CLR vectors — group barycenter."""
    if not h_list:
        return []
    D = len(h_list[0])
    n = len(h_list)
    return [sum(h[j] for h in h_list) / n for j in range(D)]


def _boundary_pressure(x, eps=1e-15):
    """max(1/x_j) — boundary pressure diagnostic."""
    x = _closure(x, eps=eps)
    return max(1.0 / v for v in x)


def _dcdi(h1, h2, carrier_names):
    """Dominant Carrier Displacement Index (Helmsman).

    sigma^Hs(t,t+1) = argmax_j |h_j(t+1) - h_j(t)|
    Returns (carrier_index, carrier_name, delta_h).
    """
    deltas = [(abs(h2[j] - h1[j]), h2[j] - h1[j], j) for j in range(len(h1))]
    deltas.sort(reverse=True)
    best = deltas[0]
    name = carrier_names[best[2]] if carrier_names else f"C{best[2]:02d}"
    return best[2], name, best[1]


