def write_csv(path, rows, fieldnames):
    """Write a list of dicts to CSV."""
    with open(path, "w", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames, extrasaction="ignore")
        writer.writeheader()
        writer.writerows(rows)


def write_group_ledger(path, group_results):
    """Write stage2_group_ledger.csv."""
    rows = []
    for g in group_results:
        row = dict(g)
        # Flatten barycenter to string
        row["group_barycenter_vector"] = ";".join(f"{v:.6f}" for v in g["group_barycenter_vector"])
        # Flatten helmsman counts
        row["helmsman_count_by_carrier"] = ";".join(
            f"{k}:{v}" for k, v in g["helmsman_count_by_carrier"].items())
        rows.append(row)

    fieldnames = ["group_id", "group_size", "group_barycenter_vector",
                  "group_metric_energy", "group_boundary_pressure",
                  "dominant_carrier", "helmsman_count_by_carrier"]
    write_csv(path, rows, fieldnames)


def write_pairwise_group_ledger(path, results):
    """Write stage2_pairwise_group_ledger.csv."""
    fieldnames = ["pair_id", "group_A", "group_B", "metric_distance",
                  "higgins_angle", "metric_energy_delta",
                  "dominant_contrast_carrier", "boundary_pressure_delta",
                  "rank", "classification"]
    write_csv(path, results, fieldnames)


def write_carrier_pair_ledger(path, results):
    """Write stage2_carrier_pair_ledger.csv."""
    fieldnames = ["carrier_pair", "co_movement_score", "opposition_score",
                  "metric_coupling_score", "dominant_day",
                  "dominant_section_view", "rank", "classification"]
    write_csv(path, results, fieldnames)


def write_section_view_ledger(path, results):
    """Write stage2_section_view_ledger.csv."""
    fieldnames = ["view_pair", "agreement_score", "divergence_score",
                  "dominant_carrier", "dominant_day", "rank", "classification"]
    write_csv(path, results, fieldnames)


def write_ranked_findings(path, group_pw, carrier_pw, section_pw):
    """Write stage2_ranked_findings.json."""
    findings = {
        "stage": "Stage 2 — HCI Metric Cross-Examination Engine",
        "top_group_divergences": [
            {k: v for k, v in r.items()}
            for r in group_pw[:5]
        ],
        "top_carrier_oppositions": [
            {k: v for k, v in r.items() if k != "carrier_i" and k != "carrier_j"}
            for r in carrier_pw[:5]
        ],
        "section_view_summary": [
            {k: v for k, v in r.items()}
            for r in section_pw
        ],
    }
    with open(path, "w") as f:
        json.dump(findings, f, indent=2)


